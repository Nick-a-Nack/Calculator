function add(a,b){
    if (!Number.isNaN(a) && !Number.isNaN(b)){
        return a + b;
    }
    else{
        console.log("Non number operand");
    }
}

function subtract(a,b){
    if (!Number.isNaN(a) && !Number.isNaN(b)){
        return a - b;
    }
    else{
        console.log("Non number operand");
    }
}

function multiply(a,b){
    if (!Number.isNaN(a) && !Number.isNaN(b)){
        return a * b;
    }
    else{
        console.log("Non number operand");
    }
}

function divide(a,b){
    if (!Number.isNaN(a) && !Number.isNaN(b)){
        return a / b;
    }
    else{
        console.log("Non number operand");
    }
}